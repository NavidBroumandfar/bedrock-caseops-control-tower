"""
J-1 human-readable report generator for completed evaluation runs.

Produces concise markdown reports from completed evaluation/safety/comparison
run results.  All functions are pure: same inputs always produce the same output.
No I/O, no AWS calls, no schema mutation.

Supported report types:
  generate_evaluation_run_report()  — F-2 batch evaluation run
  generate_safety_run_report()      — H-2 safety suite run
  generate_comparison_run_report()  — I-2 baseline vs. optimized comparison run

A report answers the key human questions quickly:
  - How many cases passed / failed?
  - What are the average scores?
  - Which cases failed or regressed?
  - What does the safety status distribution look like?

Public surface:
  generate_evaluation_run_report(run_result)
  generate_safety_run_report(results, summary, *, suite_id)
  generate_comparison_run_report(run_result, *, run_id)

Separation rules:
  - No boto3, no AWS calls.
  - No filesystem I/O — callers are responsible for writing output.
  - Only imports: evaluation runner result types and safety/comparison schemas.
"""

from __future__ import annotations

from app.evaluation.comparison_runner import ComparisonRunResult
from app.evaluation.runner import EvaluationRunResult
from app.evaluation.safety_suite import SafetyCaseResult, SafetySuiteSummary


# ── Internal formatting helpers ────────────────────────────────────────────────


def _md_table(headers: list[str], rows: list[list[str]]) -> str:
    """Render a compact markdown table from headers and rows (all strings)."""
    header_row = "| " + " | ".join(headers) + " |"
    separator = "| " + " | ".join("---" for _ in headers) + " |"
    data_rows = ["| " + " | ".join(cells) + " |" for cells in rows]
    return "\n".join([header_row, separator] + data_rows)


def _pct(numerator: int, denominator: int) -> str:
    """Format a count/total as a percentage string, e.g. '71.4%'."""
    if denominator == 0:
        return "N/A"
    return f"{(numerator / denominator) * 100:.1f}%"


def _fmt_score(score: float) -> str:
    return f"{score:.4f}"


def _fmt_delta(delta: float) -> str:
    prefix = "+" if delta > 0 else ""
    return f"{prefix}{delta:.4f}"


def _status_distribution_table(distribution: dict[str, int]) -> str:
    """Render a status → count table, sorted by status name."""
    if not distribution:
        return "_No status data available._"
    rows = [[status, str(count)] for status, count in sorted(distribution.items())]
    return _md_table(["Status", "Count"], rows)


# ── Evaluation run report ─────────────────────────────────────────────────────


def generate_evaluation_run_report(run_result: EvaluationRunResult) -> str:
    """
    Generate a markdown report for a completed F-2 evaluation run.

    The report includes:
      - run ID and timestamp
      - pass/fail counts and pass rate
      - average overall score
      - failing case IDs with their scores
      - per-metric average scores (when available)
    """
    summary = run_result.summary
    total = summary.total_cases
    passed = summary.passed_cases
    failed = summary.failed_cases

    lines: list[str] = []
    lines.append("# Evaluation Run Report\n")
    lines.append(f"**Run ID:** {summary.run_id}  ")
    lines.append(f"**Timestamp:** {summary.timestamp}\n")

    lines.append("## Summary\n")
    summary_rows = [
        ["Total Cases", str(total)],
        ["Passed", str(passed)],
        ["Failed", str(failed)],
        ["Pass Rate", _pct(passed, total)],
        ["Average Score", _fmt_score(summary.average_score)],
    ]
    lines.append(_md_table(["Metric", "Value"], summary_rows))

    lines.append("\n## Failing Cases\n")
    failed_results = [r for r in run_result.results if not r.pass_fail]
    if not failed_results:
        lines.append("_All cases passed._")
    else:
        for r in sorted(failed_results, key=lambda x: x.case_id):
            lines.append(f"- **{r.case_id}** — overall score: {_fmt_score(r.overall_score)}")

    if summary.per_metric_averages:
        lines.append("\n## Per-Metric Averages\n")
        metric_rows = [
            [metric, _fmt_score(avg)]
            for metric, avg in sorted(summary.per_metric_averages.items())
        ]
        lines.append(_md_table(["Metric", "Average Score"], metric_rows))

    lines.append(
        "\n---\n_Report generated by J-1 artifact writer. "
        "All results are offline and deterministic._"
    )
    return "\n".join(lines)


# ── Safety suite report ───────────────────────────────────────────────────────


def generate_safety_run_report(
    results: list[SafetyCaseResult],
    summary: SafetySuiteSummary,
    *,
    suite_id: str = "unknown",
) -> str:
    """
    Generate a markdown report for a completed H-2 safety suite run.

    The report includes:
      - suite ID
      - pass/fail counts and pass rate
      - safety status distribution across all evaluated cases
      - failing case IDs with their expected vs. actual status
    """
    total = summary.total
    passed = summary.passed
    failed = summary.failed

    lines: list[str] = []
    lines.append("# Safety Suite Report\n")
    lines.append(f"**Suite ID:** {suite_id}  ")
    lines.append(f"**Total Cases:** {total}\n")

    lines.append("## Summary\n")
    summary_rows = [
        ["Total Cases", str(total)],
        ["Passed", str(passed)],
        ["Failed", str(failed)],
        ["Pass Rate", _pct(passed, total)],
    ]
    lines.append(_md_table(["Metric", "Value"], summary_rows))

    lines.append("\n## Safety Status Distribution\n")
    distribution: dict[str, int] = {}
    for r in results:
        status_val = r.actual_status.value
        distribution[status_val] = distribution.get(status_val, 0) + 1
    lines.append(_status_distribution_table(distribution))

    lines.append("\n## Failing Cases\n")
    failing = [r for r in results if not r.passed]
    if not failing:
        lines.append("_All cases passed._")
    else:
        for r in failing:
            expected = r.expected_status.value
            actual = r.actual_status.value
            missing = ", ".join(c.value for c in r.missing_issue_codes)
            detail = f"expected `{expected}`, got `{actual}`"
            if missing:
                detail += f", missing codes: {missing}"
            lines.append(f"- **{r.case_id}** — {detail}")

    lines.append(
        "\n---\n_Report generated by J-1 artifact writer. "
        "All results are offline and deterministic._"
    )
    return "\n".join(lines)


# ── Comparison run report ─────────────────────────────────────────────────────


def generate_comparison_run_report(
    run_result: ComparisonRunResult,
    *,
    run_id: str = "unknown",
) -> str:
    """
    Generate a markdown report for a completed I-2 baseline vs. optimized comparison run.

    The report includes:
      - run ID and case counts
      - verdict distribution (improved / regressed / unchanged)
      - baseline vs. optimized average scores and pass counts
      - per-case verdict listing
      - safety status distribution for both sides
    """
    summary = run_result.summary

    lines: list[str] = []
    lines.append("# Comparison Run Report\n")
    lines.append(f"**Run ID:** {run_id}  ")
    lines.append(f"**Total Cases Scored:** {summary.total_cases}\n")

    lines.append("## Summary\n")
    delta_str = _fmt_delta(summary.average_score_delta)
    summary_rows = [
        ["Total Cases", str(summary.total_cases)],
        ["Improved", str(len(summary.improved_case_ids))],
        ["Regressed", str(len(summary.regressed_case_ids))],
        ["Unchanged", str(len(summary.unchanged_case_ids))],
        ["Baseline Avg Score", _fmt_score(summary.baseline_average_score)],
        ["Optimized Avg Score", _fmt_score(summary.optimized_average_score)],
        ["Average Score Delta", delta_str],
        ["Baseline Pass Count", str(summary.baseline_pass_count)],
        ["Optimized Pass Count", str(summary.optimized_pass_count)],
    ]
    lines.append(_md_table(["Metric", "Value"], summary_rows))

    lines.append("\n## Verdict Details\n")

    lines.append("### Improved Cases\n")
    if summary.improved_case_ids:
        for cid in summary.improved_case_ids:
            lines.append(f"- {cid}")
    else:
        lines.append("_None._")

    lines.append("\n### Regressed Cases\n")
    if summary.regressed_case_ids:
        for cid in summary.regressed_case_ids:
            lines.append(f"- {cid}")
    else:
        lines.append("_None._")

    lines.append("\n### Unchanged Cases\n")
    if summary.unchanged_case_ids:
        for cid in summary.unchanged_case_ids:
            lines.append(f"- {cid}")
    else:
        lines.append("_None._")

    lines.append("\n## Per-Case Results\n")
    if run_result.case_results:
        case_rows = [
            [
                r.case_id,
                r.verdict,
                _fmt_score(r.baseline_score),
                _fmt_score(r.optimized_score),
                _fmt_delta(r.score_delta),
                "yes" if r.safety_status_changed else "no",
            ]
            for r in run_result.case_results
        ]
        lines.append(
            _md_table(
                ["Case ID", "Verdict", "Baseline", "Optimized", "Delta", "Safety Changed"],
                case_rows,
            )
        )
    else:
        lines.append("_No cases were scored._")

    if run_result.missing_baseline_case_ids or run_result.missing_optimized_case_ids:
        lines.append("\n## Missing Cases\n")
        if run_result.missing_baseline_case_ids:
            missing_b = ", ".join(run_result.missing_baseline_case_ids)
            lines.append(f"**Missing baseline:** {missing_b}")
        if run_result.missing_optimized_case_ids:
            missing_o = ", ".join(run_result.missing_optimized_case_ids)
            lines.append(f"**Missing optimized:** {missing_o}")

    lines.append("\n## Safety Status Distribution\n")
    all_statuses = sorted(
        set(summary.baseline_safety_distribution) | set(summary.optimized_safety_distribution)
    )
    if all_statuses:
        dist_rows = [
            [
                s,
                str(summary.baseline_safety_distribution.get(s, 0)),
                str(summary.optimized_safety_distribution.get(s, 0)),
            ]
            for s in all_statuses
        ]
        lines.append(_md_table(["Status", "Baseline", "Optimized"], dist_rows))
    else:
        lines.append("_No status data available._")

    lines.append(
        "\n---\n_Report generated by J-1 artifact writer. "
        "All results are offline and deterministic._"
    )
    return "\n".join(lines)
